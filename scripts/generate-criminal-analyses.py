def clean(text: str, limit: int = 72) -> str:
    text = re.sub(r"\s+", "", text)
    return text if len(text) <= limit else text[:limit] + "……"


def topic_for(question: dict) -> tuple[str, str, list[str], str]:
    haystack = question["prompt"] + " " + " ".join(question["options"])
    for pattern, issue, rule, articles, trap in TOPICS:
        if re.search(pattern, haystack):
            return issue, rule, articles, trap
    return DEFAULT


def build_seed(question: dict) -> dict:
    issue, rule, articles, trap = topic_for(question)
    accepted = question.get("acceptedAnswers") or ([] if question["answer"] is None else [question["answer"]])
    labels = "、".join(chr(65 + index) for index in accepted)
    answer_texts = "；".join(clean(question["options"][index], 56) for index in accepted)
    if question.get("allCredit"):
        application = "考選部更正本題為一律給分，表示原選項無法維持單一正解；作答時仍應依上述要件逐一檢驗。"
    elif len(accepted) > 1:
        application = f"考選部更正接受 {labels}。兩個選項分別為「{answer_texts}」，顯示原題在要件或表述上容許複數判斷。"
    else:
        application = f"題幹重點為「{clean(question['prompt'])}」。依上述要件檢驗，官方答案 {labels} 的「{answer_texts}」最符合命題時法。"
    return {
        "issue": f"{issue}：本題應如何依刑法要件判斷？",
        "rule": rule,
        "application": application,
        "trap": trap,
        "articles": articles,
        "confidence": "中",
    }


