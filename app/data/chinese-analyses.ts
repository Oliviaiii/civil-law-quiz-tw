export function buildChineseAnalysis(record: ChineseQuestionRecord) {
  const note = notes[record.id];
  if (!note || record.answer === null) return undefined;
  const answerLabel = String.fromCharCode(65 + record.answer);
  return {
    confidence: "中" as const,
    analysis: {
      issue: note.point,
      rule: note.rule,
      application: note.explanation,
      conclusion: `依考選部標準答案，應選 ${answerLabel}「${record.options[record.answer]}」。`,
      trap: note.distractors,
    },
  };
}

